def count_letters(text):
    """
    Функция подсчитывает количество букв в тексте (регистр не учитывается).
    Возвращает словарь, где ключи — буквы в нижнем регистре, а значения — их количество.
    """
    # Приводим текст к нижнему регистру и инициализируем пустой словарь
    text = text.lower()
    letter_count = {}

    # Проходим по каждому символу текста
    for char in text:
        if char.isalpha():  # Проверяем, является ли символ буквой
            if char in letter_count:
                letter_count[char] += 1
            else:
                letter_count[char] = 1
    return letter_count
# TODO Напишите функцию calculate_frequency
def calculate_frequency(letter_count):
    """
    Функция вычисляет частоту каждой буквы.
    Принимает словарь с буквами и их количеством, возвращает словарь с частотами.
    """
    total_letters = sum(letter_count.values())  # Общее количество букв
    frequency = {}

    for letter, count in letter_count.items():
        frequency[letter] = count / total_letters  # Вычисляем частоту буквы
    return frequency

def print_frequencies(frequencies):
    """
    Функция печатает частоты букв в столбик, округляя значения до двух знаков после запятой.
    """
    for letter, freq in frequencies.items():
        print(f"{letter}: {freq:.2f}")

